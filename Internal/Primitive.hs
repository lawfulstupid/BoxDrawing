
module BoxDrawing.Internal.Primitive (
   horizontal, vertical,
   up, down, left, right,
   heavy, double
) where

import BoxDrawing.Internal.Util

horizontal = '─'
vertical   = '│'
up = '╵'
down = '╷'
left = '╴'
right = '╶'

dbL = '\10001'
dbU = '\10002'
dbR = '\10003'
dbD = '\10004'

heavy :: Char -> Char
heavy c
   | c == ' ' = c
   | c?:('─','━') || c=='╼' || c=='╾' = '━'
   | c?:('│','┃') || c=='╽' || c=='╿' = '┃'
   | c?:('╌','╍') = '╍'
   | c?:('╎','╏') = '╏'
   | c?:('┄','┅') = '┅'
   | c?:('┆','┇') = '┇'
   | c?:('┈','┉') = '┉'
   | c?:('┊','┋') = '┋'
   | c `elem` "╵╹" = '╹'
   | c `elem` "╷╻" = '╻'
   | c `elem` "╴╸" = '╸'
   | c `elem` "╶╺" = '╺'
   | c?:('┌','┏') || c?:('╒','╔') = '┏'
   | c?:('┐','┓') || c?:('╕','╗') = '┓'
   | c?:('└','┗') || c?:('╘','╚') = '┗'
   | c?:('┘','┛') || c?:('╛','╝') = '┛'
   | c?:('├','┣') || c?:('╞','╠') = '┣'
   | c?:('┤','┫') || c?:('╡','╣') = '┫'
   | c?:('┬','┳') || c?:('╤','╦') = '┳'
   | c?:('┴','┻') || c?:('╧','╩') = '┻'
   | c?:('┼','╋') || c?:('╪','╬') = '╋'
   | otherwise = errInvalidChar c

double :: Char -> Char
double c
   | c == ' ' = c
   | c?:('─','━') || c=='╼' || c=='╾' = '═'
   | c?:('│','┃') || c=='╽' || c=='╿' = '║'
   | c `elem` "╵╹" = dbU
   | c `elem` "╷╻" = dbD
   | c `elem` "╴╸" = dbL
   | c `elem` "╶╺" = dbR
   | c?:('┌','┏') || c?:('╒','╔') = '╔'
   | c?:('┐','┓') || c?:('╕','╗') = '╗'
   | c?:('└','┗') || c?:('╘','╚') = '╚'
   | c?:('┘','┛') || c?:('╛','╝') = '╝'
   | c?:('├','┣') || c?:('╞','╠') = '╠'
   | c?:('┤','┫') || c?:('╡','╣') = '╣'
   | c?:('┬','┳') || c?:('╤','╦') = '╦'
   | c?:('┴','┻') || c?:('╧','╩') = '╩'
   | c?:('┼','╋') || c?:('╪','╬') = '╬'
   | otherwise = errInvalidChar c
